package com.objective.multilinkmgr.model;

public class AddressTemplateBean {
    private String templateid;
    private String templatecode;
    private String templatedescr;
    private String templateactive;

    public String getTemplateid() {
        return templateid;
    }

    public void setTemplateid(String templateid) {
        this.templateid = templateid;
    }

    public String getTemplatecode() {
        return templatecode;
    }

    public void setTemplatecode(String templatecode) {
        this.templatecode = templatecode;
    }

    public String getTemplatedescr() {
        return templatedescr;
    }

    public void setTemplatedescr(String templatedescr) {
        this.templatedescr = templatedescr;
    }

    public String getTemplateactive() {
        return templateactive;
    }

    public void setTemplateactive(String templateactive) {
        this.templateactive = templateactive;
    }
}
